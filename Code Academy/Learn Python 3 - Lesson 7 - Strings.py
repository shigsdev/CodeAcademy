def username_generator(first_name, last_name):
    username = ""
    if len(first_name) < 3:
        p1 = first_name
    else:
        p1 = first_name[:3]
    if len(last_name) < 4:
        p2 = last_name
    else:
        p2 = last_name[:4]
    username = p1 + p2
    return username

# Great work! Now for the temporary password, they want the function to take the input user name and shift all of the
# letters by one to the right, so the last letter of the username ends up as the first letter and so forth. For
# example, if the username is AbeSimp, then the temporary password generated should be pAbeSim.
#
# Start by defining the function password_generator so that it takes one input, username and in it define an empty
# string named password.
#
# Inside password_generator create a for loop that iterates through the indices username by
# going from 0 to len(username).
#
# To shift the letters right, at each letter the for loop should add the previous letter to the string password.


def password_generator(username):
    password = ""
    l = len(username)
    for x in range(l):
        password += username[x-1]
    return password

# Write a function called common_letters that takes two arguments, string_one and string_two and then returns a list
# with all of the letters they have in common.
#
# The letters in the returned list should be unique. For example,
#
# common_letters("banana", "cream")
# should return ['a'].

def common_letters(string_one, string_two):
    combined_string = []
    for letter in string_one:
        if letter in string_two and letter not in combined_string:
            combined_string.append(letter)
    return combined_string


print(common_letters("banana", "cream"))

